    <h1>Machu Picchu</h1>
</div>
<div class="parrafo">
    <img class="abajo" src="http://blog.redbus.pe/wp-content/uploads/2020/06/peru-3978148_1920.jpg" alt="">

    <p>Pocos lugares hay en el Perú que despiertan tanto interés como el espectáculo que representa Machu Picchu para todo el que tenga la fortuna de ir alguna vez en la vida a esta belleza de sitio. Este es uno de esos lugares que siempre debe estar incluido en cualquier lista de destinos preferidos del país andino, así que aquí tenía que estar. Esta belleza de santuario histórico se ha convertido en un verdadero ícono para todo peruano del mundo, ya que el orgullo que despierta en todos los ciudadanos de esta nación simplemente no puede ser descrito con palabras. Es por todo el interés que ha generado por parte del resto del planeta que se declaró el santuario como Patrimonio Histórico de la Humanidad en la década de 1980 y no es para menos que así haya resultado ser, de hecho mencionar al mencionar al Perú sin duda alguna nos trae a la mente este hermosa maravilla del mundo que sin duda sigue siendo uno de los principales lugares turísticos del perú . Una de las principales razones por las que ha ganado tanta importancia es por la arqueología tan compleja que envuelve a este lugar, pero a eso hay que sumarle todos los ecosistemas que hacen vida en el sitio. Eso sin mencionar la diversidad en flora y fauna que se puede hallar aquí y que ha dejado impactados a los expertos del mundo entero, quienes se han dejado maravillar por el espectáculo natural que Machu Picchu les ofrece.</p>
</div>


<div>
    <h1>Cuzco</h1>
</div>
<div class="parrafo">   <img class="abajo" src="https://1rygll2mc0uf2r05dx165m4k-wpengine.netdna-ssl.com/wp-content/uploads/2015/01/cuzco-3.jpg" alt="">

    <p>Cuzco es uno de los lugares muy importantes e interesantes que deberias conocer es pori se está buscando un lugar que contenga lo más valioso del país a nivel histórico y cultural, el mejor sitio de todos es Cuzco, el cual conserva muchas de las características con las que contaba el país en la época del Imperio Inca en el plano arquitectónico. Hay muchas obras de tipo civil y religioso que dejan ver todas y cada una de las riquezas del periodo colonial. Tal y como ha ocurrido con Machu Picchu y el Centro Histórico de Lima, Cuzco también se declaró como Patrimonio de la Humanidad por la UNESCO, debido a que es mucho lo que conserva de la etapa colonial intacto. Quienes han ido afirman que estar allí es como revivir esa etapa de la vida cultural y política del Perú sin haber estado presente. A todo eso hay que sumarle una gran diversidad de comercios, restaurantes, bares, cafeterías, mercados y demás lugares que hacen aún más amena la visita. Si lo que se quiere es vivir en carne propia y lo más sentida e intensamente posible todo lo que ha sido la historia del Perú, hay que ir a Cuzco a ver todo lo que sus tantas plazas y palacios coloniales tiene que decir.</p>
